public class Vetor01 {

    public static void main(String[] args) {

        int[] n = {3,2,8,7,5,4};
        System.out.println(n.length); // Imprime o tamanho do vetor.
        for(int c=0; c<=5; c++) {
            System.out.println("Na posição " + c + " temos o valor "+ n[c]);
        }
    }
}
